public class Main {

    public static void main(String[] args) {
        // вызов функций
        sayHello();
        sayGoodbye();
    }

    /**
     * Приветствие
     */
    static void sayHello() {
        System.out.println("Привет!");
    }

    /**
     * Прощание
     */
    static void sayGoodbye() {
        System.out.println("До скорого!");
    }
}